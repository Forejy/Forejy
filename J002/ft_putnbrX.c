void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putchar((nb % 10) + 48);
	}
	else
	{
		ft_putchar(nb + 48);
	}
}

int		main(void)
{
	ft_putnbr(INT_MAX);
	return (0);
}

